
public class CustomVal {

    public int ival;
    public double dval;
    public String sval;
    public Object obj;

    public CustomVal() {}
    public CustomVal(int val) { ival=val; }
    public CustomVal(double val) { dval=val; }
    public CustomVal(String val) { sval=val; }
    public CustomVal(Object val){ obj=val; }
}